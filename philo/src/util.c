#include "philo.h"

int ft_atoi(const char *arr) {
	int res;
	int sign;

	res = 0;
	sign = 1;
	if (*arr == '-')
	{
		sign = -1;
		arr++;
	}
	while (*arr >= '0' && *arr <= '9')
		res = res * 10 + *arr++ - '0';
	return (res * sign);
}
